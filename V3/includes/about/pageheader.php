			<p class="page-description">Heddoko, Japanese for head coach, was born a year ago from an idea at the precise intersection of sports, technology, and passion – in the mind of a software engineer training for a mixed martial arts (MMA) competition in a Montreal gym.  

 A seasoned athlete with a passion for martial arts, Brazilian-born Mazen Elbawab is the perfect and unlikely balance of tech expert and sports nut. An experienced software engineer and developer with experience in both the medical and entertainment industries, Mazen became a motion-control design engineer at leading video game developer Ubisoft. A motion capture expert, he was one of the first programmers to create games for the Xbox Kinect system. 

 While training for an MMA competition, unable to have a coach to help him one-on-one whenever he needed, he dreamt of a way to have an expert monitor his movement and give him feedback to improve. The idea for Heddoko was born.  

Mazen brought his idea to Concordia’s District 3 Innovation Center, and soon had a Co-Founder, established entrepreneur and business operations expert Alexandre Fainberg, and a team to help him work towards his goal. 

Officially founded in May of 2014, Heddoko became not just a company, but a community of professionals bound by pushing the boundaries of human performance and the belief in the power of the human spirit. 

<br><em>We are Heddoko. We live to play, to train, to practice, to explore new ideas, and to challenge ourselves to make good things great. 
